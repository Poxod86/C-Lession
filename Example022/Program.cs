// метод ввода картинки 
void printImage(int[,] img)
{
  for (int i = 0; i < img.GetLength(0); i++)
  {
    for (int j = 0; j < img.GetLength(1); j++)
    {
      if (img[i, j] == 1)
      {
        Console.Write($"*");
      }
      else
      {
        Console.Write($" ");
      }
    }
    Console.WriteLine();
  }
}

printImage(pic);

// метод закрашивание картинки

void fillImage(int row, int col)
{
  if (pic[row, col] == 0)
  {
    pic[row, col] = 1;
    fillImage(row - 1, col);
    fillImage(row, col - 1);
    fillImage(row + 1, col);
    fillImage(row, col + 1);
  }
}

